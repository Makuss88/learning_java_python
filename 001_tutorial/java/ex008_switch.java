public class ex008_switch {

  public static void main(String[] args) {
    String letter = "c";

    switch (letter) {
      case "a":
        System.out.println("letter a");
        break;
      case "b":
        System.out.println("b");
        break;
      case "c":
        System.out.println("c");
        break;
      case "d":
        System.out.println("d");
        break;
      default:
        System.out.println("??");
    }
  }
}
